// String seperti teks simpelnya, jadi secara garis besar ini seperti kumpulan karakter , walaupun itu 0 tetapi diubah menjad "0" maka otomatis jadi string
console.log('0'); // Output : 0
console.log('My Name is Arul'); // Output : My Name is Arul
console.log('My Name is Kiyo' + 'taka'); // Output : My Name is Kiyotaka
console.log('My Name is Kiyo' + '\nFrom : XI TKJ 2'); // \n berguna untuk menjadi paragrahp
console.log('My Name is Kiyo' + '\n\tFrom : XI TKJ 2'); // \t berguna untuk tab
console.log('My Name is \"Kiyo\" '); // \" berguna untuk petik
console.log('Call Me Kiyo \\ Arul'); // \\ untuk tanda (\)
console.log('\'Ini Tanda petik satu \'') // \' untuk tanda (')