/* Operator matematika di Javascript
+ = tambah
- = kurang
* = kali
** = pangkat
/ = bagi
% = sisa bagi

Operator Augmented Assigment
+= tambah
-= kurang
*= kali
**= pangkat
/= bagi
%= sisa bagi

Operator Unary
+ nilai positif
- nilai negatif
++ increment (menaikan 1 angka)
-- decrement (menurunkan 1 angka)
*/
let num  = 4 + 3;
console.log('4 + 3 = ' + num);
num += 7;
console.log('7 + 7 = ' + num);
num ++
console.log('14 + 1 = '+ num);
num --
console.log('15 - 1 = ' + num);